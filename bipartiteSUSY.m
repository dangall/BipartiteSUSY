(*For convenience we'll also append to each of these pairs the two node numbers that they represent*)
cutcoordinatesandnodes=MapThread[{#1,#2}&,{cutcoordinates,Map[Alternatives@@#&,cutcoordinates/.coordstononumbers]}];
