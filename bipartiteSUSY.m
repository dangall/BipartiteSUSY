externaladjacencyseed={{0,1},{1,0}};
,If[permutations[[1]]==={1},
externaladjacencyseed={1};
,externaladjacencyseed=Normal[AdjacencyMatrix[PathGraph[Prepend[permutations[[1]],extnum]]]];
];
];
externaladjencyattempts=Map[externaladjacencyseed[[#,#]]&,permutations];
